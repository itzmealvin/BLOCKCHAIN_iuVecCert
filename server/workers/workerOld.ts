import {genProof, genVerifierContractParams} from "../libs/lib-kzg";
import {parentPort, workerData} from "worker_threads";
import FileParamsDto from "../dtos/FileParamsDto";

function getProofs(
    coeffs: bigint[],
    commit: bigint[],
    works: FileParamsDto[],
    worker: number,
) {
    return works.map((work, index) => {
        const {fileIndex, fileHash} = work;
        const proof = genProof(coeffs, parseInt(fileIndex));
        const params = genVerifierContractParams(
            commit,
            proof,
            parseInt(fileIndex),
            BigInt(fileHash),
        );
        const timestamp = new Date().toISOString();
        console.log(`PROOFS: Worker ${worker} processed file index ${index} at timestamp: ${timestamp}`);
        return {fileProof: params.proof, fileIndex: params.index, fileHash: params.value};
    })
}

